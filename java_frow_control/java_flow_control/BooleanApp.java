package java_flow_control;

public class BooleanApp {

	public static void main(String[] args) {
		
		System.out.println("one"); //문자열 String
		System.out.println(1); //정수 int
		
		System.out.println(true);
		System.out.println(false);
		// true와 false는 쓰임이 있는 키워드, 
        //앞으로 그럴 가능성이 있는 키워드를 reserved word라고함
		
		String foo = "Hello world";
//		String true = "Hello world";
		
		System.out.println(foo.contains("world"));
		System.out.println(foo.contains("loklok2"));

	}

}
